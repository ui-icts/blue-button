    @Before
    public void setUp() throws Exception {
    	super.setUp();
    	int id = 0;
        this.bluebuttonDaoService.getLabResultSynonymService().save(new LabResultSynonym(id++, "CO2", "Carbon Dioxide", "Electrolyte"));
        this.bluebuttonDaoService.getLabResultSynonymService().save(new LabResultSynonym(id++, "Chloresterol Total", "Cholesterol", ""));
    }

    @After
    public void tearDown() throws Exception {
    	super.tearDown();
    }
    
    @Test
    public void labResultSynonymDaoShouldReturnListOfLabResultSynonymsWhenGivenListOfUnofficialName(){
    	Set<String> set = new HashSet<String>();
    	set.add("Carbon Dioxide"); set.add("Cholesterol");
    	List<LabResultSynonym> lrsList = this.bluebuttonDaoService.getLabResultSynonymService().getSynonym(set);
    	assertEquals(2, lrsList.size());
    	LabResultSynonym lrs = lrsList.get(0);
    	assertEquals("CO2", lrs.getOfficialName()); assertEquals("Carbon Dioxide", lrs.getUnofficialName()); assertEquals("Electrolyte", lrs.getPanel()); 
    	lrs = lrsList.get(1);
    	assertEquals("Chloresterol Total", lrs.getOfficialName()); assertEquals("Cholesterol", lrs.getUnofficialName());
    }
    
